name="toki3122"
print(len(name))
print(name.find("i"))#to find the pos of some substring
print(name.capitalize())#capitalizes the first alphabet
print(name.upper())#turns the string upper case
print(name.lower())#turns the string lower case
print(name.isdigit())#to check if the string has only digit
print(name.isalpha())#to check if the string has only alphabets
print(name.count("o"))#counts the number of some string
print(name.replace("o","a"))#replaces some substring
print(name*3)